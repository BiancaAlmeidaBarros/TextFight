package Codigos;

public class Personagem {
    int atq;
    int vida;

    public Personagem(int atq, int vida) {
        this.atq = atq;
        this.vida = vida;
    }

    public int getAtq() {
        return atq;
    }

    public void setAtq(int atq) {
        this.atq = atq;
    }

    public int getVida() {
        return vida;
    }

    public void setVida(int vida) {
        this.vida = vida;
    }
    
    
}
